import math

''' The calculate module contain functions to calculate radians
    that performs the mathematics and returns the values to sine,
    cosine and tangent. '''


def calculate_sin(radianos):

	''' Return the sine of x radians. '''
	return math.sin(radianos)
    
def calculate_cos(radianos):

	''' Return the cosine of x radians. '''
	return math.cos(radianos)

def calculate_tan(radianos):

	''' Return the tangent of x radians. '''
	return math.tan(radianos)

def calculate_acos(r):
	return math.acos(r)

def calculate_asin(r):
	return math.asin(r)

def calculate_atan(r):
	return math.atan(r)

